// models/Shader.js
const mongoose = require('mongoose');

const ShaderSchema = new mongoose.Schema({
    user: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    title: {
        type: String,
        required: true,
        trim: true
    },
    code: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    }
}).index({ user: 1, title: 1 }, { unique: true });

module.exports = mongoose.model('Shader', ShaderSchema);